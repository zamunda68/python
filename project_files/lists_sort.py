# Sorting lists of strings and integers in alphabetical/ascending way with the .sort() method
# There are two basic lists
# 1. "numbers" list
numbers = [42, 17, 23, 13, 12]  # < created list with numbers inside
numbers.sort(reverse=False)  # < sort numbers in ascending list
print(numbers)  # < print out the sorted list

# 2. "cars" list
cars = ["Volvo", "BMW", "Alfa_Romeo", "Audi", "Mercedes"]  # < created list with strings inside
cars.sort(reverse=False) # < sort numbers in ascending list
print(cars)  # < print out the sorted list
